-- Do not rename function name and arguments order
-- due to localization purposes.
-- This file format hard coded in localization script Localizer\inputEvents-xgettext.lua
local inputEvents = {}

local function addEvent(key, value)
  if inputEvents[key] then
    print("inputEvents[" .. key .. "] already exists!")
  else
    if value then
      inputEvents[key] = value
    else
      print("Key[" .. key .. "] has no value!")
    end
  end
end

