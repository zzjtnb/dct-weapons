function getDisplayName(a_type)
    if DB.isInitialized() == false then 
        return a_type
    end

    local unitTypeDesc = DB.unit_by_type[a_type]
    
    if unitTypeDesc and unitTypeDesc.DisplayName then
        return unitTypeDesc.DisplayName
    end
    return a_type
end

